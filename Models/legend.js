class Legend {
    motoSentence
    nickName
    description
    about
    realName
    age
    HomeWorld
    tacticalAbility
    passiveAbility
    ultimateAbility
    legendImage
    constructor(motoSentence,
        nickName,
        description,
        about,
        realName,
        age,
        HomeWorld,
        tacticalAbility,
        passiveAbility,
        ultimateAbility,
        legendImage) {
        this.motoSentence=motoSentence
        this.nickName=nickName
        this.description=description
        this.about=about
        this.realName=realName
        this.age=age
        this.HomeWorld=HomeWorld
        this.tacticalAbility=tacticalAbility
        this.passiveAbility=passiveAbility
        this.ultimateAbility=ultimateAbility
        this.legendImage=legendImage
    }

}
module.exports=Legend